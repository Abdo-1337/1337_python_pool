def ft_list_comprehension():
    """
    Demonstrates list comprehensions for data filtering and transformation.

    - Filters players with high scores.
    - Doubles player scores.
    - Extracts active players.
    """
    high_scores = [player for player, score in data["players"].items()
                   if score["total_score"] > 2000]
    scores_doubled = [score['total_score'] * 2 for score
                      in data["players"].values()]
    active_players = [player for player, mode in data["players"].items()
                      if mode['is_active'] is True]

    print("=== List Comprehension Examples ===")
    print("High scores (> 2000):", high_scores)
    print("Scores doubled:", scores_doubled)
    print("Active players", active_players)


def ft_dict_comprehenstion():
    """
    Demonstrates dictionary comprehensions for analytics.

    - Maps players to their scores.
    - Counts score categories.
    - Maps players to achievement counts.
    """
    categories = ['high', 'medium', 'low']

    player_scores = {player: score['total_score'] for player, score
                     in data["players"].items()}
    score_categories_list = [x['score_category'] for x in data["sessions"]]
    score_categories = {
        score: len([s for s in score_categories_list if s == score])
        for score in categories}
    achievement_counts = {player: count["achievements_count"]
                          for player, count in data["players"].items()}

    print("=== Dict Comprehension Examples ===")
    print("Player scores:", player_scores)
    print("Score categories:", score_categories)
    print("Achievement counts:", achievement_counts)


def ft_set_comprehenstion():
    """
    Demonstrates set comprehensions for uniqueness.

    - Extracts unique players.
    - Identifies unique achievements.
    - Collects active regions.
    """
    unique_players = {player["player"] for player in data["sessions"]}
    unique_achievements = {ach for ach in data["achievements"]}
    active_regions = {reg["region"] for reg in data["players"].values()}

    print("=== Set Comprehension Examples ===")
    print("Unique players:", unique_players)
    print("Unique achievements:", unique_achievements)
    print("Active regions:", active_regions)
    print(active_regions)


def ft_top_performer(player):
    """
    Displays analytics for the top-performing player.

    - Retrieves score and achievement count.
    - Formats top player summary.
    """
    player_name = player[0]
    for name, info in data["players"].items():
        if name == player_name:
            player_score = info["total_score"]
            player_ach = info["achievements_count"]

    print(
        f"Top performer: {player_name} ({player_score} points,"
        f"{player_ach} achievements)"
        )


def ft_combined_analysis():
    """
    Combines multiple comprehensions for global analytics.

    - Calculates total players and achievements.
    - Computes average score.
    - Identifies the top performer.
    """
    total_players = len([player for player in data["players"].keys()])
    total_unique_achievements = len(set(data["achievements"]))
    total_score = (sum([score["total_score"]
                        for score in data["players"].values()]))
    average_score = total_score / total_players

    top_player = [
        player for player, score in data["players"].items()
        if score["total_score"] == max([s["total_score"]
                                        for s in data["players"].values()])]

    print("=== Combined Analysis ===")
    print("Total players:", total_players)
    print("Total unique achievements:", total_unique_achievements)
    print(f"Average score: {average_score:.2f}")
    ft_top_performer(top_player)


