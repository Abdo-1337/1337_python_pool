def event_processor(id: int, player: str, level: str, action: str):
    """
    Formats a single game event into a readable string.
    """
    return (f"Event {id}: Player {player} (level {level}) {action}")


def stream_processor(event_list: list, count: int):
    """
    Generates a stream of game events using a generator.

    - Cycles through a finite event list.
    - Reuses events if count exceeds list size.
    - Yields one formatted event at a time.
    """
    total_events = len(event_list)
    index = 0
    while index < count:
        event = event_list[index % total_events]
        yield (event_processor(
            event['id'],
            event['player'],
            event['data']['level'],
            event['data']['action']
        ))
        index += 1


def print_data_stream(event_list, generates_num, print_num):
    """
    Displays a preview of the generated game data stream.

    - Processes a given number of events.
    - Prints only the first few events for demonstration.
    """
    print("=== Game Data Stream Processor ===\n")
    print(f"Processing {generates_num} game events...\n")
    test = stream_processor(event_list, generates_num)
    for i in range(print_num):
        print(next(test))
        if i == print_num - 1:
            print("...")


def stream_analytics(event_list: list, count: int):
    """
    Analyzes streamed game events.

    - Counts total processed events.
    - Tracks high-level player events (level >= 10).
    - Counts treasure and level-up events.
    - Demonstrates analytics on streamed data.
    """
    total_events = len(event_list)
    index = 0
    treasure_event_count = 0
    level_up_count = 0
    high_level = 0
    while index <= count:
        event = event_list[index % total_events]
        if event['data']['level'] >= 10:
            high_level += 1
        if event['data']['action'] == "found treasure":
            treasure_event_count += 1
        if event['data']['action'] == "leveled up":
            level_up_count += 1
        if event['event_type'] == "level_up":
            level_up_count += 1
        index += 1

    print("=== Stream Analytics ===")
    print("Total events processed:", count)
    print("High-level players (10+):", high_level)
    print("Treasure events:", treasure_event_count)
    print("Level-up events:", level_up_count)


