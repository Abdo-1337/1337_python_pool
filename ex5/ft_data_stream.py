def ft_fibonacci(number: int):
    """
    Generates Fibonacci numbers using a generator.

    - Produces numbers lazily.
    - Uses constant memory.
    """
    a, b = 0, 1
    for i in range(number):
        yield a
        a, b = b, a + b


def ft_prime(number: int):
    """
    Generates prime numbers using a generator.

    - Tests numbers incrementally.
    - Yields primes one at a time.
    - Stops after the requested count.
    """
    current = 2
    while number != 0:
        for i in range(2, current):
            if current % i == 0:
                break
        else:
            yield current
            number -= 1
        current += 1


def generators(fibo_nbr: int, prime_nbr: int):
    """
    Demonstrates generator usage with Fibonacci and prime sequences.
    """
    fibo_result = ft_fibonacci(fibo_nbr)
    prime_result = ft_prime(prime_nbr)

    print("=== Generator Demonstration ===")
    print(f"Fibonacci sequence (first {fibo_nbr}):", end=" ")
    index = 1
    for i in fibo_result:
        print(i, end="")
        if (index < fibo_nbr):
            print(", ", end="")
        else:
            print()
        index += 1

    print(f"Prime numbers (first {prime_nbr}):", end=" ")
    index = 1
    for nbr in prime_result:
        print(nbr, end="")
        if (index < prime_nbr):
            print(", ", end="")
        else:
            print()
        index += 1


